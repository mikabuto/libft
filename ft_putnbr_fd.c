#include "libft.h"
#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	int		len;
	int		i;

	i = -1;
	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	len = ft_num_len(n);
	while (++i < len)
	{
		ft_putchar_fd((n / ft_power(len - i - 1) % 10) + '0', fd);
	}
}
